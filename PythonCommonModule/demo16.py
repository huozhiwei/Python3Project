# 数学函数

import math
print('圆周率','=',math.pi)
print('自然常数','=',math.e)

# 取绝对值
print(math.fabs(-1.2))

# 向上取整
print(math.ceil(1.3))

# 向下取整
print(math.floor(1.8))

# 计算幂
print(math.pow(2,10))

# 计算平方根
print(math.sqrt(8))

# 计算正弦，余弦和正切，函数参数是弧度,即包含math.pi的参数
print(math.sin(math.pi/2))
print(math.cos(math.pi))
print(math.tan(math.pi/4))